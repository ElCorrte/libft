#include "libft.h"

void	ft_nbrstr(int n, char *str, int i)
{
	long int t;

	t = n;
	if (t < 0)
	{
		str[i] = '-';
		t *= -1;
	}
	i++;
	if (t > 9)
	{
		ft_nbrstr(t / 10, str, i);
	}
	str[i] = t % 10 + 48;
}
