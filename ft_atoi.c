#include "libft.h"

int		ft_atoi(const char *str)
{
	int b;
	int d;

	b = 0;
	while (ft_isspace(*str) == 1)
		str++;
	d = (*str == '-' ? -1 : 1);
	if (*str == '-' || *str == '+')
		str++;
	while (*str >= 48 && *str <= 57)
	{
		b = b * 10 + (*str - '0');
		str++;
	}
	return (b * d);
}
