#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long int t;

	t = n;
	if (t < 0)
	{
		ft_putchar_fd('-', fd);
		t *= -1;
	}
	if (t > 9)
	{
		ft_putnbr_fd(t / 10, fd);
	}
	ft_putchar_fd(t % 10 + 48, fd);
}
