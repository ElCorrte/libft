#include "libft.h"

int		ft_nbri(int n)
{
	long int	t;
	int			cnt;

	cnt = 0;
	t = n;
	if (t <= 0)
	{
		cnt++;
		t *= -1;
	}
	while (t > 0)
	{
		t /= 10;
		cnt++;
	}
	return (cnt);
}
