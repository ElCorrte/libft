#include "libft.h"

void	ft_putnbr(int n)
{
	long int t;

	t = n;
	if (t < 0)
	{
		ft_putchar('-');
		t *= -1;
	}
	if (t > 9)
	{
		ft_putnbr(t / 10);
	}
	ft_putchar(t % 10 + 48);
}
